CREATE TABLE IF NOT EXISTS Phone (
    ID INT AUTO_INCREMENT PRIMARY KEY,
   	CORRELATION_ID VARCHAR(255) NOT NULL,
    BRAND VARCHAR(255) NOT NULL,
    MODEL VARCHAR(255) NOT NULL,
    OPERATING_SYSTEM VARCHAR(255),
    PROCESSOR VARCHAR(255),
    RAM INT,
    STORAGE INT,
    CONNECTIVITY VARCHAR(255),
    BATTERY_CAPACITY INT,
    DISPLAY_SIZE DECIMAL(10,2),
    DISPLAY_TYPE VARCHAR(255),
    REFRESH_RATE INT,
    REAR_CAMERA_RESOLUTION INT,
    FRONT_CAMERA_RESOLUTION INT,
    NUMBER_OF_CAMERA INT,
    BLUETOOTH_VERSION DECIMAL(3,1),
    DUAL_SIM BOOLEAN,
    COLOR VARCHAR(255),
    FACE_UNLOCK BOOLEAN,
    FINGER_PRINT_SENSOR BOOLEAN,
    PRICE DECIMAL(10,2),
    RELEASED_DATE DATE,
    SPECIFICATION BLOB NOT NULL
);
